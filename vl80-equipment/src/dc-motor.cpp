double DCMotor::cPhi(double If)
{
    double cPhi = 0.0;

    for (size_t i = 0; i < a.size(); ++i)
    {
        double b_i = cPhi_nom * a[i] / pow(Inom, i+1);

        cPhi += b_i * pow(If, i+1);
    }

    return cPhi;
}
